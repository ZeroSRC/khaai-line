type Dict = typeof dict.th

// Build a union of all valid dot-notation keys, e.g. 'nav.home'
type DotKeys<T, P extends string = ''> = {
  [K in keyof T & string]: T[K] extends Record<string, unknown>
    ? DotKeys<T[K], `${P}${K}.`>
    : `${P}${K}`
}[keyof T & string]

export type TKey = DotKeys<Dict>

function resolve(lang: Lang, key: string): string {
  const parts = key.split('.')
  let node: unknown = dict[lang]
  for (const p of parts) {
    if (node && typeof node === 'object' && p in node) {
      node = (node as Record<string, unknown>)[p]
    } else {
      return key // missing key → show the key itself for easy spotting
    }
  }
  return typeof node === 'string' ? node : key
}

export type TFn = (key: TKey, params?: Record<string, string | number>) => string

export function useT(): TFn {
  const lang = useLangStore((s) => s.lang)
  return (key, params) => {
    let out = resolve(lang, key)
    if (params) {
      for (const [k, v] of Object.entries(params)) {
        out = out.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v))
      }
    }
    return out
  }
}

/** Non-hook accessor for use outside React (rare). */
export function translate(lang: Lang, key: TKey, params?: Record<string, string | number>): string {
  let out = resolve(lang, key)
  if (params) for (const [k, v] of Object.entries(params)) out = out.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v))
  return out
}
