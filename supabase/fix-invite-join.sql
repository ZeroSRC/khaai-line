-- FIX: ลิงก์เชิญสมาชิกใช้งานไม่ได้ (chicken-and-egg ที่ RLS)
-- ─────────────────────────────────────────────────────────────
-- อาการ: เปิดลิงก์ /shop/[slug]/join แล้วขึ้น "ไม่พบร้าน" ทั้งที่ร้านมีจริง
--
-- สาเหตุ: policy อ่าน `shops` คือ is_shop_member(id)
--   → ต้องเป็นสมาชิก "ก่อน" ถึงจะอ่านร้านได้
--   แต่คนที่กดลิงก์เชิญยัง "ไม่ใช่สมาชิก" → อ่านร้านไม่ได้ → สมัครไม่ได้ → วนไม่จบ
--
-- แก้: ฟังก์ชัน security definer ที่ resolve ร้านจาก slug ได้โดยไม่ต้องเป็นสมาชิก
--   ปลอดภัย: slug อยู่ในลิงก์เชิญอยู่แล้ว การคืน id+name จาก slug ที่รู้อยู่แล้วไม่เปิดเผยอะไรเพิ่ม
--   คืนแค่ id + name เท่านั้น (ไม่ใช่ทั้งแถว — กันข้อมูลตั้งค่าร้านรั่ว)
-- ─────────────────────────────────────────────────────────────

create or replace function shop_public_by_slug(p_slug text)
returns table (id uuid, name text)
language sql
security definer
set search_path = public
as $$
  select id, name from shops where slug = p_slug limit 1;
$$;

-- เปิดให้เรียกได้ทั้งคนที่ล็อกอินแล้วและ anon (คนใหม่ที่ยังไม่มี JWT ของร้านนี้)
grant execute on function shop_public_by_slug(text) to anon, authenticated;

